#Receba do usuário a quantidade de litros de combustível consumidos e a distância percorrida.Calcule e imprima o consumo médio em km/l.

l = int(input('Digite a quantidade de combustível consumido - litros: '))
km = int(input('Digite a distância percorrida - quilômetos: '))
medio = km/l

print (f'Olá, teve um consumo médio de {medio} km/l')