#Faça um Programa que peça dois números, realize as principais operações: soma, subtração, multiplicação,divisão

numero1 = int(input('Digite o número 1: '))
numero2 = int(input('Digite o número 2: '))
soma = numero1 + numero2
print(f'A soma dos números digitados é: {soma}') 

sub = numero1 - numero2
print(f'A subtração dos números digitados é: {sub}')

mult = numero1 * numero2
print(f'A multiplicação dos números digitados é: {mult}')

div = numero1 / numero2
print(f'A divisão dos números digitados é: {int(div)}')

