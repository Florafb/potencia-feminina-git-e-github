#Faça um Programa que peça a quantidade de quilômetros, transforme em metros,centímetros e milímetros.

qui = float(input('Digite os quilômetros que se deseja converter: '))
metros = qui * (1000)
print(f'Metros: {metros}') 

cent = qui * (10000)
print(f'Centímetros: {cent}') 

mil = qui * (100000)
print(f'Milímetros: {mil}') 



